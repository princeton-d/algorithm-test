package org.example;

import java.util.*;

public class _0_직각_삼각형_출력하기 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        
        for (int i = 1; i <= n; i++) {
            System.out.println("*".repeat(i));
        }
    }
}
